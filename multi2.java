public class multi2 {

    public static int analyse(String word) {
        if (word.charAt(0) == 't') {
            if (word.charAt(1) == 'e') {
                if (word.charAt(2) == 's') {
                    if (word.charAt(3) == 't') {
                            return 1;
                        }
                    }
                }
            }

            return 0;
    }
}